<div><h1 class="top"style="font-size:26px;">HTML Introduction</h1></div>
<div ><h2>What is HTML?</h2>
<ul>
<li style="text-align: left;"><strong>HTML</strong> is an initialism for "<strong>HyperText Markup Language"</strong>.</li>
<li style="text-align: left;">It is the language of the web.</li>
<li style="text-align: left;">It is used to create websites.</li>
<li style="text-align: left;">It is used to define a page layout, meaning it is a barebone page structure.</li>
<li style="text-align: left;"><strong>HTML</strong> is used for making pages of the website also called webpages that we see on the internet</li>
<li style="text-align: left;">It consists of a set of tags.</li>
<li style="text-align: left;">This set of tags is written in HTML Document.</li>
<li style="text-align: left;"><strong>".html"</strong>&nbsp;or <strong>".htm"</strong> is the extension.</li>
<li style="text-align: left;">There are so many versions of <strong>HTML</strong> but <strong>HTML5</strong> is the latest version.</li>
</ul>
<h2>&nbsp;</h2>

