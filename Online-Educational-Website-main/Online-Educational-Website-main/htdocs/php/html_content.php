<ul>
<li>In <strong>1989</strong>, <span style="color: rgb(230, 126, 35);"><strong>Tim Berners Lee</strong></span> established <span style="color: rgb(35, 111, 161);">www</span> and created <span style="color: rgb(230, 126, 35);"><strong>HTML</strong></span> in <strong>1991</strong>.&nbsp;</li>
<li>From <strong>1995 to 1997, </strong>he started working on versions of HTML.</li>
<li>In <strong>1999</strong>, a committee was organized that made <strong>HTML4.0</strong> a standard.</li>
