<h2>&nbsp</h2>
<h2>Features of HTML</h2>
<ul>
<li>It is platform-independent.</li>
<li>Images, videos, and audio can be added to a web page.</li>
<li>Hypertext can be added to the text.</li>
<li>It is a markup language.</li>
<li>It is interpreted language</li>
<li>It can be integrated with other languages like CSS, JavaScript etc.</li>
<li>Semantic Structure.</li>
<li>local Storage &amp; Indexed DB – Client-side data storage.</li>
<li>Offline Capabilities (PWA) with Cache API &amp; Service Workers.</li>
</ul>
<h2><br>Why the word HyperText &amp; Markup Language</h2>
<p>The word hypertext markup language comprises the words <strong>“hypertext”</strong> and <strong>“markup language”</strong>. The term <strong>"hypertext"</strong> refers to the linking of text with other documents and <strong>“markup language”</strong> refers to a language that uses a set of tags.</p>
<p>So, <strong>HTML</strong> is the linking of text with other documents using some set of tags.</p>
<p>&nbsp;</p>
<p><span style="color: rgb(230, 126, 35);">Note:- </span>Tags refers to some meaningful texts enclosed in angle braces&nbsp;<code>&lt;...&gt;</code>. For eg. <code>&lt;head&gt;</code>. Each tag has a different meaning and importance in building an HTML page which can affect the web page in its own ways&nbsp;</p>
